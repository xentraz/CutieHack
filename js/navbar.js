function openNav() {
    document.getElementById('sidebar-nav').style.width = '100%';
    document.getElementById('open_tray').style.display = 'none';
}

function closeNav() {
    document.getElementById('sidebar-nav').style.width = '0%';
    document.getElementById('open_tray').style.display = 'block';
}

